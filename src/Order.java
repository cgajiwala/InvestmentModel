
public class Order {

	private String sec;
	private char trans;
	private float diffAmount;
	
	
	

	public String getSec() {
		return sec;
	}
	public void setSec(String sec) {
		this.sec = sec;
	}
	public char getTrans() {
		return trans;
	}
	public void setTrans(char trans) {
		this.trans = trans;
	}
	public float getDiffAmount() {
		return diffAmount;
	}
	public void setDiffAmount(float diffAmount) {
		this.diffAmount = diffAmount;
	}
	
	
	
	
}
